// Update user profile
router.put('/profile', [
  body('firstName').optional().notEmpty().withMessage('First name cannot be empty'),
  body('lastName').optional().notEmpty().withMessage('Last name cannot be empty'),
  body('email').optional().isEmail().withMessage('Valid email is required'),
  body('addresses').optional().isArray().withMessage('Addresses must be an array')
], async (req, res) => {
  try {
    const token = req.header('Authorization')?.replace('Bearer ', '');
    
    if (!token) {
      return res.status(401).json({
        success: false,
        message: 'Access denied. No token provided.'
      });
    }

    const decoded = jwt.verify(token, process.env.JWT_SECRET || 'fallback-secret');
    const user = await User.findById(decoded.userId);

    if (!user) {
      return res.status(404).json({
        success: false,
        message: 'User not found'
      });
    }

    const errors = validationResult(req);
    if (!errors.isEmpty()) {
      return res.status(400).json({
        success: false,
        message: 'Validation errors',
        errors: errors.array()
      });
    }

    // Update user basic fields
    user.firstName = req.body.firstName || user.firstName;
    user.lastName = req.body.lastName || user.lastName;
    user.email = req.body.email || user.email;
    user.updatedAt = new Date();

    // Optionally update addresses
    if (Array.isArray(req.body.addresses)) {
      const allowedTypes = ['home', 'work', 'other']
      const sanitized = []
      for (const addr of req.body.addresses) {
        const item = {
          type: allowedTypes.includes((addr?.type || '').toLowerCase()) ? (addr.type || 'home') : 'other',
          phone: addr?.phone,
          address: addr?.address,
          city: addr?.city,
          state: addr?.state,
          zipCode: addr?.zipCode,
          country: addr?.country || 'USA',
          isDefault: Boolean(addr?.isDefault)
        }
        // Validate required fields
        if (!item.address || !item.city || !item.state || !item.zipCode || !item.country) {
          return res.status(400).json({
            success: false,
            message: 'Invalid address: address, city, state, zipCode, and country are required'
          })
        }
        sanitized.push(item)
      }
      // Ensure one default
      if (!sanitized.some(a => a.isDefault) && sanitized.length > 0) {
        sanitized[0].isDefault = true
      } else if (sanitized.filter(a => a.isDefault).length > 1) {
        // If multiple defaults sent, keep the first one as default
        let found = false
        for (const a of sanitized) {
          if (a.isDefault && !found) { found = true; continue }
          a.isDefault = false
        }
      }
      user.addresses = sanitized
    }

    await user.save();

    // Remove password from response
    const userResponse = user.toObject();
    delete userResponse.password;

    res.json({
      success: true,
      message: 'Profile updated successfully',
      data: userResponse
    });
  } catch (error) {
    res.status(500).json({
      success: false,
      message: 'Error updating profile',
      error: error.message
    });
  }
});

// Change password
router.put('/change-password', [
  body('currentPassword').notEmpty().withMessage('Current password is required'),
  body('newPassword').isLength({ min: 6 }).withMessage('New password must be at least 6 characters'),
  body('confirmPassword').custom((value, { req }) => {
    if (value !== req.body.newPassword) {
      throw new Error('Password confirmation does not match new password');
    }
    return true;
  })
], async (req, res) => {
  try {
    const token = req.header('Authorization')?.replace('Bearer ', '');
    
    if (!token) {
      return res.status(401).json({
        success: false,
        message: 'Access denied. No token provided.'
      });
    }

    const decoded = jwt.verify(token, process.env.JWT_SECRET || 'fallback-secret');
    const user = await User.findById(decoded.userId);

    if (!user) {
      return res.status(404).json({
        success: false,
        message: 'User not found'
      });
    }

    const errors = validationResult(req);
    if (!errors.isEmpty()) {
      return res.status(400).json({
        success: false,
        message: 'Validation errors',
        errors: errors.array()
      });
    }

    const { currentPassword, newPassword } = req.body;

    // Verify current password
    const isCurrentPasswordValid = await bcrypt.compare(currentPassword, user.password);
    if (!isCurrentPasswordValid) {
      return res.status(400).json({
        success: false,
        message: 'Current password is incorrect'
      });
    }

    // Hash new password
    const salt = await bcrypt.genSalt(10);
    const hashedNewPassword = await bcrypt.hash(newPassword, salt);

    // Update password
    user.password = hashedNewPassword;
    user.updatedAt = new Date();

    await user.save();

    res.json({
      success: true,
      message: 'Password changed successfully'
    });
  } catch (error) {
    res.status(500).json({
      success: false,
      message: 'Error changing password',
      error: error.message
    });
  }
});

